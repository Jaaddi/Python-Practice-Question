def volume_finder(l, w, h):
    """The Function will take the parameter
    l = Length of the Parallelepiped
    w = Width of the Parallelepiped
    h = Height of the Parallelepiped"""
    return l * w * h
l = int(input("Enter the Length of the Parallelepiped"))
w = int(input("Enter the Width of the Parallelepiped"))
h = int(input("Enter the Heigth of the Parallepiped"))
volume_finder(l, w, h)
